
public class Quiz_02 {

	public static void main(String[] args) {

		int a = 1325;
		int b = 9327;
		System.out.println("1번 문제 정답 : " + a * b);
		// 자바에서도 연산자 우선순위는 수학과 동일하게 적용됨.
		// 자바에서 문자열끼리 + 연산은 연결을 의미
		
		long c = 10000000000L + 5000;
		System.out.println(c);

		char d = 'A';
		char e = 'B';
		System.out.print(d + " " + e);
	}
}


