import java.lang.*;

public class Exam_01 { // 파일명과 똑같이
	public static void main(String[] ar) { // 초기 실행위치
		// Hello World를 출력하는 코드. () 안에 출력할 내용을 작성.
		System.out.println("Hello World");
		System.out.println("Java Study! 한글도 되나?");
		System.out.println(10);
		System.out.println(3.14);
		System.out.println('A');
		// 자바에서 문자열을 표현할 때는 "" 를 사용한다.
		// 자바에서 한글자를 표현할 때는 '' 를 사용한다.
		// 자바에서 숫자를 표현할 때는 no quote (따옴표 안씀)
	}
}
